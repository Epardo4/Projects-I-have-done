
public class BreakNode extends StatementNode{
	/**
	 * Empty Constructor for the BreakNode class
	 */
	public BreakNode() {}
	/**
	 * Displays information for the given break statement
	 * @return break statement display
	 */
	public String toString() {
		return "break;";
	}
	/**
	 * Does nothing 
	 */
	public void exceptionToThrow(String exception) {}
}
